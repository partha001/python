print(int(3.99)) #op: 3  .i.e. float to int.note this is not rounding.
print(int("3")) #op: 3  .i.e. string to integer


print(float(4.99)) #op: 4.99
print(float(3))  #op: 3.0  .i.e. int to float conversion
print(float("7.789")) #op: 7.789  i.e. string to float conversion


print(str("10.5")) #op: 10.5 
print(str(10)) #op: 10
print(str("10.5"))
