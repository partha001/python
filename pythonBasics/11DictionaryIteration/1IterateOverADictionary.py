indianFood = {
    "chickenBiriyani": 100,
    "chillyChicken": 80,
    "friedRice" : 50
}

for food in indianFood:
    print(f"The food is {food} and its price is {indianFood[food]}")
#op: 
# The food is chickenBiriyani and its price is 100
# The food is chillyChicken and its price is 80
# The food is friedRice and its price is 50


